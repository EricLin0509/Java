class HelloWorld { // class 是一个关键字，用来定义一个类
    public static void main(String[] args) { // 主方法，程序的入口
        /*
        public 是一个访问修饰符，用于控制类、方法、变量等的访问权限 
        static 是一个关键字，用于定义静态方法或变量
        void 是一个关键字，用于定义方法的返回类型，表示方法不返回任何值
        String[] 表示传入的参数类型为字符串数组
        */
        System.out.println("Hello World!"); // System.out.println 是一个方法，用于输出字符串到控制台
        // 输出 Hello World!
    }
}
